class IsLostPassphraseCaseController {
  /**
   * Constructor
   * @param {Worker} worker
   * @param {string} requestId uuid
   */
  constructor(worker, requestId) {
    this.worker = worker;
    this.requestId = requestId;
  }

  /**
   * Wrapper of exec function to run it with worker.
   *
   * @return {Promise<void>}
   */
  async _exec() {
    try {
      const result = await this.exec();
      this.worker.port.emit(this.requestId, "SUCCESS", result);
    } catch (error) {
      console.error(error);
      this.worker.port.emit(this.requestId, 'ERROR', error);
    }
  }

  /**
   * Check if the recover is in lost-passphrase case.
   *
   * @return {Promise<boolean>}
   */
  async exec() {
    const url = new URL(this.worker.tab.url);
    const caseParam = url.searchParams.get("case");
    return caseParam === "lost-passphrase";
  }
}

export default IsLostPassphraseCaseController;
